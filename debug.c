const char *find_reg_name(ULONG id)
{
	struct id_name *i;
	for ( i = reg_names; i -> id; i++ )
	{
		if (i -> id == id) return i-> name;
	}

	return "Unkown";
}

const char *id_names[max_table_size];

void init_qfind_lookup()
{
	int i;
	for (i=0;i<max_table_size;i++) id_names[i] = find_reg_name( 0xDFF000 | (i<<1) );
}

